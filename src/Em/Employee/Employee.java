package Em.Employee;

public class Employee {

private String name;
private int age;
private long salary;

String setName(String name)
{
    this.name=name;
    return this.name;
}
public int setAge(int age)
{
    this.age=age;
    return this.age;
}

public long setSalary(long salary)
{
    this.salary=salary;
    return this.salary;
}
public String  getName()
{
    return name;
}
public int getAge()
{
    return age;
}
public long getSalary()
{
    return salary;
}













}
